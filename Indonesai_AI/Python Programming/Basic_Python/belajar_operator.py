# Tentukan berapa hasil dari operasi +, -, *, /, %, // dan ** dari kedua variable di bawah!
# Lalu bandingkan hasil yang ada dengan nilai 30 dengan perbandingan >, <, ==, !=, >=, <= 

variable1 = 0.25
variable2 = 20

# Operasi untuk menghitung variable diatas
kali = variable1*variable2
print("Ini hasil dari operasi perkalian", variable1, "dan", variable2, "adalah", kali)
# Perbandingan hasil operasi dengan 30
banding = kali > 30
print("Hasil perbandingan antara", kali, "dengan 30 adalah", banding)